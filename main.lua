local Create = assert(LoadLibrary("RbxUtility").Create)

local LetterSpacing = 15 -- measured in pixels
local CurrentSpace	= 0

--==--

function DrawPixel(coordinate, color, parent)
	local pixel = Create "Frame" {
		Parent = parent;
		Size = UDim2.new(0, 2, 0, 2);
		BackgroundColor3 = color;
		Position = coordinate;
	}
	pixel.BorderSizePixel = 0
	return pixel
end

function DrawChar(char, parent)
	local charCarrier = Create "Frame" {Parent = parent}
	if char ~= " " then
		for i=1, #FontPackage[char] do
			DrawPixel( UDim2.new(0, FontPackage[char][i][1] + CurrentSpace, 0, FontPackage[char][i][2]), Color3.new(1, 1, 1), charCarrier )
		end
	end
	CurrentSpace = CurrentSpace + LetterSpacing
end

function Out(msg, ...)
	return print(string.format(msg, unpack({...})))
end

function DrawSentence(sentence, parent)
	for i=1, #sentence do
		if not FontPackage[sentence:sub(i,i)] and sentence:sub(i,i)~=" " then Out("Unable to find character '%s'", sentence:sub(i,i)) return end
		if sentence:sub(i, i + 1) == "." or sentence:sub(i, i) == "." or sentence:sub(i, i + 1) == "," or sentence:sub(i, i) == "," then
			LetterSpacing = 5
		end
		if sentence:sub(i, i) == "d" then
			LetterSpacing = 17
		end
		if sentence:sub(i, i) == "v" then
			LetterSpacing = 18
		end
		DrawChar(sentence:sub(i,i), parent)
		LetterSpacing = 15
	end
end

